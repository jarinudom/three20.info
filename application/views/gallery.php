UH Guide
--------

<div class="source">Created by: CampusGuide, University of Houston CS Dept.</div>

<div class="appstore" markdown=1><a href="http://itunes.apple.com/ca/app/uh-guide/id347382575?mt=8">![UH Guide](/gfx/appstore.png 230x80)</a></div>

The UH Guide iPhone application assists you in finding your way around the University of Houston
(main campus) as well as staying updated with what is going on on-campus.

### Links

* [Website](http://uhcamp.us.to/blog/)

### Three20 Components

* [TTLauncherView](http://api.three20.info/interface_t_t_launcher_view.html)
* [TTNavigator](/ui/navigation)
* [TTURLRequest](http://api.three20.info/interface_t_t_u_r_l_request.html): caching for our "points of interest" requests
* [TTPhotoViewController](http://api.three20.info/interface_t_t_photo_view_controller.html): nice thumbnail photos from Flickr
* [TTTabBar](http://api.three20.info/interface_t_t_tab_bar.html): switching between content
* [TTTables](http://api.three20.info/interface_t_t_table_view_controller.html): tweets

